CREATE TABLE URL_SCANS (
ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
URL VARCHAR2(500),
RESULT VARCHAR2(20),
TRUST_SCORE NUMBER,
SCAN_DATE DATE DEFAULT SYSDATE
);

CREATE TABLE EMAIL_SCANS (
ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
EMAIL_TEXT CLOB,
RESULT VARCHAR2(20),
SCAN_DATE DATE DEFAULT SYSDATE
);

CREATE TABLE IMAGE_SCANS (
ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
IMAGE_NAME VARCHAR2(200),
EXTRACTED_TEXT CLOB,
RESULT VARCHAR2(20),
SCAN_DATE DATE DEFAULT SYSDATE
);